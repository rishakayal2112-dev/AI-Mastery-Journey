#append()
fruits = ["Apple", "Mango"]
fruits.append("Orange")
print(fruits)

#insert()
fruits = ["Apple", "Orange"]
fruits.insert(1, "Mango")
print(fruits)

#remove()
fruits = ["Apple", "Mango", "Orange"]
fruits.remove("Mango")
print(fruits)

#pop()
fruits = ["Apple", "Mango", "Orange"]
fruits.pop()
print(fruits)

#sort()
numbers = [5, 1, 8, 2]
numbers.sort()
print(numbers)

#reverse()
numbers = [1, 2, 3, 4]
numbers.reverse()
print(numbers)
